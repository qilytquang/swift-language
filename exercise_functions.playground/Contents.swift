func add(_ num1: Double, _ num2: Double) -> Double {
    return num1 + num2
}

func subtract(_ num1: Int, _ num2: Int) -> Int {
    return num1 - num2
}

func multiply(_ num1: Float, _ num2: Float) -> Float {
    return num1 * num2
}

func divide(_ num1: Double, _ num2: Double) -> Double {
    return num1 / num2
}

